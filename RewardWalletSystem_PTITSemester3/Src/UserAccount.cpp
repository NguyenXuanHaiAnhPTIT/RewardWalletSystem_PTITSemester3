#include "../include/UserAccount.h"
#include <iostream>
#include "../sha256/Sha256.h"
#include "../Include/Wallet.h"
#include "../Include/FileManager.h"
using namespace std;

// Constructor mặc định
UserAccount::UserAccount()
    : username(""), hashedPassword(""), fullName(""), email(""), phone(""),
    isAdmin(false), isLocked(false), isPasswordAutoGenerated(false)
{
}

// Constructor đầy đủ
UserAccount::UserAccount(const std::string& username,
    const string& hashedPassword,
    const string& fullName,
    const string& email,
    const string& phone,
    bool isAdmin,
    bool isLocked,
    bool isPasswordAutoGenerated)
    : username(username),
    hashedPassword(hashedPassword),
    fullName(fullName),
    email(email),
    phone(phone),
    isAdmin(isAdmin),
    isLocked(isLocked),
    isPasswordAutoGenerated(isPasswordAutoGenerated)
{
}

void UserAccount::displayInfo() const {
    cout << "------ User Information ------\n";
    cout << "Username    : " << username << "\n";
    cout << "Full Name   : " << fullName << "\n";
    cout << "Email       : " << email << "\n";
    cout << "Phone       : " << phone << "\n";
    cout << "Role        : " << (isAdmin ? "Admin" : "User") << "\n";
    cout << "Status      : " << (isLocked ? "Locked" : "Active") << "\n";
    cout << "Password Type : " << (isPasswordAutoGenerated ? "Auto" : "Custom") << "\n";
    // Hiển thị số dư ví
    Wallet w;
    if (FileManager().loadWallet(username, w)) {
        cout << "Wallet Balance: " << w.balance << " diem\n";
    }
    else {
        cout << "Wallet Balance: [Khong the tai thong tin vi]\n";
    }
}

bool UserAccount::checkPassword(const std::string& inputPassword) const {
    return hashedPassword == Sha256(inputPassword);
}